            <h2>Intership/Project/Thesis</h2>
            Internships (Industrial Attachment) are essential components 
            of training and are undertaken by all students from first year
             to the last year. They are important because they help set f
             oundation for students careers. Internships are unique opportunities 
             that may orientate students on the selection of the topic of a project or
              thesis. Interns are monitored to ensure that these essential 
            components of training are undertaken by all students before graduation. <br>
            <h2>Organisation of Academic Programs and Evaluation</h2> <br>
            Training programmes are structured in semesters and each semester 
            may have courses, internships, a project or a Thesis with a maximum 
            of 36 credits. Courses are sub-divided into lectures, tutorials and 
            or practical and each course is allocated a certain number of credits. 
            The evaluation consists of continuous assessments and a written exam. 
            The continuous assessment is scored over 30 points and the exam over
             70 points making a total of 100 pts. The BMP 
            grading system with a GPA over 4 is applied to determine the final grade of the 
            certificate awarded.
